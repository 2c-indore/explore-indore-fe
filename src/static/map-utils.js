import * as d3 from 'd3-collection';

// Helpers
export const nester = (data) => {
  const nested = d3.nest()
    .key((d) => { return d.section; })
    .entries(data);
  // console.log(nested);

  const obj = {};
  nested.forEach((item) => {
    const array = [];
    item.values.forEach((value) => {
      array.push(value);
    });
    obj[item.key] = array;
  });

  return obj;
};


